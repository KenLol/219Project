package FantasyBaseball;

/**
 *
 * @author Kenneth
 */
public class Hitter {
    
    private String first;           //FIRST_NAME
    private String last;            //LAST_NAME
    private String proteam;         //TEAM
    private String position;        //QP
    private String year;            //YEAR_OF_BIRTH
    private String r;               //R
    private String hr;              //HR
    private String rbi;             //RBI
    private String sb;              //SB
    private String note;            //NOTES
    private String ba;              //H/AB
    
    private String ab;              //AB
    private String h;               //H
    
    public Hitter(String first, String last){
        this.first = first;
        this.last = last;
    }

    /**
     * @return the first
     */
    public String getFirst() {
        return first;
    }

    /**
     * @param first the first to set
     */
    public void setFirst(String first) {
        this.first = first;
    }

    /**
     * @return the last
     */
    public String getLast() {
        return last;
    }

    /**
     * @param last the last to set
     */
    public void setLast(String last) {
        this.last = last;
    }

    /**
     * @return the proteam
     */
    public String getProteam() {
        return proteam;
    }

    /**
     * @param proteam the proteam to set
     */
    public void setProteam(String proteam) {
        this.proteam = proteam;
    }

    /**
     * @return the position
     */
    public String getPosition() {
        return position;
    }

    /**
     * @param position the position to set
     */
    public void setPosition(String position) {
        this.position = position;
    }

    /**
     * @return the year
     */
    public String getYear() {
        return year;
    }

    /**
     * @param year the year to set
     */
    public void setYear(String year) {
        this.year = year;
    }

    /**
     * @return the r
     */
    public String getR() {
        return r;
    }

    /**
     * @param r the r to set
     */
    public void setR(String r) {
        this.r = r;
    }

    /**
     * @return the hr
     */
    public String getHr() {
        return hr;
    }

    /**
     * @param hr the hr to set
     */
    public void setHr(String hr) {
        this.hr = hr;
    }

    /**
     * @return the rbi
     */
    public String getRbi() {
        return rbi;
    }

    /**
     * @param rbi the rbi to set
     */
    public void setRbi(String rbi) {
        this.rbi = rbi;
    }

    /**
     * @return the sb
     */
    public String getSb() {
        return sb;
    }

    /**
     * @param sb the sb to set
     */
    public void setSb(String sb) {
        this.sb = sb;
    }

    /**
     * @return the note
     */
    public String getNote() {
        return note;
    }

    /**
     * @param note the note to set
     */
    public void setNote(String note) {
        this.note = note;
    }

    /**
     * @return the ba
     */
    public String getBa() {
        return ba;
    }

    /**
     * @param ba the ba to set
     */
    public void setBa(String ba) {
        this.ba = ba;
    }

    /**
     * @return the ab
     */
    public String getAb() {
        return ab;
    }

    /**
     * @param ab the ab to set
     */
    public void setAb(String ab) {
        this.ab = ab;
    }

    /**
     * @return the h
     */
    public String getH() {
        return h;
    }

    /**
     * @param h the h to set
     */
    public void setH(String h) {
        this.h = h;
    }
    
    
}
